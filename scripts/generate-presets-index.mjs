#!/usr/bin/env node
/**
 * generate-presets-index.mjs — scan packages/engine-ass/src/presets/
 * for *.ts files (other than index.ts) and emit a fresh index.ts that
 * imports each one's `definition` export and registers it in
 * DEFINITIONS in the order: free → basic → premium → (filename order
 * within each tier).
 *
 * Run after install-pack.sh swaps the pack contents.
 */
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const PRESETS_DIR = join(__dirname, '..', 'packages', 'engine-ass', 'src', 'presets');
const OUT = join(PRESETS_DIR, 'index.ts');

const TIER_ORDER = { free: 0, basic: 1, premium: 2 };

const files = readdirSync(PRESETS_DIR)
  .filter((f) => f.endsWith('.ts') && f !== 'index.ts');

const entries = files.map((f) => {
  const src = readFileSync(join(PRESETS_DIR, f), 'utf8');
  // Look for: tier: 'free' | 'basic' | 'premium' inside the definition object
  const m = src.match(/tier:\s*'(free|basic|premium)'/);
  if (!m) {
    throw new Error(`Preset ${f} is missing a "tier:" field in its definition export.`);
  }
  // Convert "clean-white.ts" → "cleanWhite" for the import alias
  const slug = f.replace(/\.ts$/, '');
  const alias = slug.replace(/-([a-z])/g, (_, c) => c.toUpperCase());
  return { file: f, slug, alias, tier: m[1] };
});

entries.sort((a, b) => {
  const t = TIER_ORDER[a.tier] - TIER_ORDER[b.tier];
  return t !== 0 ? t : a.slug.localeCompare(b.slug);
});

const importLines = entries
  .map((e) => `import { definition as ${e.alias} } from './${e.slug}';`)
  .join('\n');

const arrayLines = entries.map((e) => `  ${e.alias},  // ${e.tier}`).join('\n');

const out = `/**
 * AUTO-GENERATED by scripts/generate-presets-index.mjs.
 * Do not edit by hand. Re-run after copying pack files into this dir.
 *
 * Order: free → basic → premium → (alphabetical within tier).
 */
import type { AssPresetBuilder, AssPresetDefinition, AssPresetTier } from '../types';

${importLines}

export const DEFINITIONS: AssPresetDefinition[] = [
${arrayLines}
];

export const PRESETS: Record<string, AssPresetBuilder> = Object.fromEntries(
  DEFINITIONS.map((d) => [d.slug, d.build]),
);

export const PRESET_DESCRIPTIONS: Record<string, string> = Object.fromEntries(
  DEFINITIONS.map((d) => [d.slug, d.description]),
);

export function tierForPreset(slug: string): AssPresetTier {
  return DEFINITIONS.find((d) => d.slug === slug)?.tier ?? 'free';
}

export function listPresets(): string[] {
  return DEFINITIONS.map((d) => d.slug);
}

export function presetsByTier(): Record<AssPresetTier, AssPresetDefinition[]> {
  const out: Record<AssPresetTier, AssPresetDefinition[]> = {
    free: [],
    basic: [],
    premium: [],
  };
  for (const d of DEFINITIONS) out[d.tier].push(d);
  return out;
}
`;

writeFileSync(OUT, out);
console.log(`Wrote ${OUT} with ${entries.length} preset(s).`);
